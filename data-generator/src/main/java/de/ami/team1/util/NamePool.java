    public static UserPojo generateUser() {
        int mOrF = RandomHelper.getInteger(0, 1);
        UserPojo userPojo = new UserPojo();
        if (mOrF == 0) {
            userPojo.setGender('m');
            userPojo.setMail(getMaleFirstname() + "." + getLastname() + getMailProvider());
        } else {
            userPojo.setGender('f');
            userPojo.setMail(getFemaleFirstname() + "." + getLastname() + getMailProvider());
        }

        userPojo.setDateOfBirth(LocalDate.now().minusDays(RandomHelper.getInteger(5840, 34675)));

        return userPojo;
    }

